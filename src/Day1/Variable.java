package Day1;
public class Variable {
    public static void main(String[] args) {

        // Variable (biến) là một vùng nhớ dùng để lưu trữ dữ liệu.

        int age = 20;

        //int   → kiểu dữ liệu (Data Type)
        //age   → tên biến (Variable Name)
        //20    → giá trị (Value)

        // Tại sao cần Variable?

        System.out.println("Boss");
        System.out.println("Boss");
        System.out.println("Boss");

        // Nếu muốn đổi tên Boss -> Natsu phải sửa 3 chỗ
        // dùng biến String

        String name = "Natsu";

        System.out.println(name);
        System.out.println(name);
        System.out.println(name);

        // Chỉ cần sửa 1 lần.
    }
}
